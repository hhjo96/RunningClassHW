package codingTestPractice.dynamicProgramming;

import java.util.Arrays;
import java.util.Scanner;

public class BJ1149 {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);

        int n = scanner.nextInt(); // 집의 개수
        int answer = 0;
        int[][] cost = new int[n][3];
        int[][] dp = new int[n][3];

        for(int i = 0; i < n; i++) {
            for(int j = 0; j < 3; j++) {
                cost[i][j] = scanner.nextInt();
            }
        }

 //       System.out.println(Arrays.toString(cost[0]));

        //초기 세팅
        dp[0][0] = cost[0][0];
        dp[0][1] = cost[0][1];
        dp[0][2] = cost[0][2];

        //둘째 줄부터 N개의 줄에는 각 집을 빨강, 초록, 파랑으로 칠하는 비용이 1번 집부터 한 줄에 하나씩 주어진다
        for (int i = 1; i < n; i++) {
            dp[i][0] = Math.min(dp[i - 1][1], dp[i - 1][2]) + cost[i][0];
            dp[i][1] = Math.min(dp[i - 1][0], dp[i - 1][2]) + cost[i][1];
            dp[i][2] = Math.min(dp[i - 1][0], dp[i - 1][1]) + cost[i][2];
        }
//        System.out.println(Arrays.toString(dp[n-1]));
        answer = Math.min(Math.min(dp[n-1][0], dp[n-1][1]), dp[n-1][2]);

        System.out.println(answer);

    }

}
